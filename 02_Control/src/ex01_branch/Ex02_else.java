package ex01_branch;

public class Ex02_else {

	
	
	public static void main(String[] args) {
	
		
		//else가 지원될때는 if문을 2번 사용하지 않는다!
		
		int score = 50;
	
		if(score >= 60) {
		System.out.println("합격");
		System.out.println("축하합니다");
	}
		
		
		else {
		System.out.println("불합격");
		System.out.println("보충수업입니다");
	
	}
		

	}

}
